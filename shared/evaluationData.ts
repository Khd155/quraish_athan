// دالة مساعدة للحصول على المسارات حسب المحور
export function getTracksByAxis(axisId: string): Track[] {
  const axis = evaluationAxes.find(a => a.id === axisId);
  return axis?.tracks ?? [];
}

// دالة مساعدة للحصول على المعايير حسب المسار
export function getCriteriaByTrack(axisId: string, trackId: string): Criterion[] {
  const tracks = getTracksByAxis(axisId);
  const track = tracks.find(t => t.id === trackId);
  return track?.criteria ?? [];
}
